public class Carro
{
    //Atributos
    public string cor;
    public string modelo;
    public int ano;

//Construtor da classe ( para inicializar os atributos )

    public Carro(string corDoCarro, string modeloDoCarro, int anoDoCarro)
    {
        cor = corDoCarro;
        modelo = modeloDoCarro;
        ano = anoDoCarro;
    }
}